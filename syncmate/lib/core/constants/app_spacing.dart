/// Centralized spacing system for the application.
/// 
/// Standardized values ensure consistent layouts across different screens
/// and simplify maintenance when updating the design system.
class AppSpacing {
  AppSpacing._();

  /// Default horizontal padding for the screen/app bar
  static const double horizontalPadding = 16.0;

  // Incremental spacing units
  static const double s2 = 2.0;
  static const double s3 = 3.0;
  static const double s4 = 4.0;
  static const double s8 = 8.0;
  static const double s10 = 10.0;
  static const double s12 = 12.0;
  static const double s16 = 16.0;
  static const double s20 = 20.0;
  static const double s24 = 24.0;
}
